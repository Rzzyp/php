<?php 
/*  *排序算法
    *由于在排序过程中总是小数往前放，大数往后放，相当于气泡往上升，所以称作冒泡排序。
    *冒泡排序大概的意思是依次比较相邻的两个数，然后根据大小做出排序，直至最后两位数。
*/
$arr=[25,34,0,7,6,99,52,9,12,11,26,37,5];

for($i=0;$i<count($arr)-1;$i++){

    for($j=0;$j<count($arr)-1-$i;$j++){
        //依次比较相邻的两个数,小数往前放，大数往后放.
        if($arr[$j]>$arr[$j+1]){
            $temp=$arr[$j];
            $arr[$j]=$arr[$j+1];
            $arr[$j+1]=$temp;
        }
    }

    
}
